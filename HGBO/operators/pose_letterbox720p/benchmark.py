#!/usr/bin/env python3
"""Strict board benchmark adapter for the PoseLetterbox720p custom operator."""

from __future__ import annotations

import json
import os
import re
import subprocess
import sys
from pathlib import Path


def run(command: list[str], cwd: Path, env: dict[str, str]) -> subprocess.CompletedProcess[str]:
    return subprocess.run(command, cwd=cwd, env=env, text=True, capture_output=True, timeout=900)


def parse_metric(name: str, output: str) -> float | None:
    match = re.search(rf"^\s*{re.escape(name)}=([0-9.]+)\s*$", output, re.MULTILINE)
    return float(match.group(1)) if match else None


def fail(message: str, output_path: Path) -> int:
    output_path.write_text(json.dumps({
        "latency_ms": 1e8,
        "throughput_fps": 0.0,
        "jitter_ms": 1e8,
        "cpu_usage": 0.0,
        "memory_usage": 0,
        "correct": False,
        "compile_status": message,
        "backend": "ascendc_npu",
    }, indent=2), encoding="utf-8")
    print(message, file=sys.stderr)
    return 1


def main() -> int:
    if len(sys.argv) != 2:
        print("Usage: benchmark.py <candidate-config.json>", file=sys.stderr)
        return 2

    config = json.loads(Path(sys.argv[1]).read_text(encoding="utf-8"))
    buffer_num = config.get("buffer_num")
    output_rows_per_tile = config.get("output_rows_per_tile")
    pipeline_mode = config.get("pipeline_mode")
    result_path = Path(__file__).with_name("benchmark_result.json")
    if (buffer_num not in (1, 2) or output_rows_per_tile not in (1, 2, 4) or
            pipeline_mode not in ("serial", "staged")):
        return fail(
            f"invalid buffer_num={buffer_num!r}, "
            f"output_rows_per_tile={output_rows_per_tile!r}, "
            f"pipeline_mode={pipeline_mode!r}",
            result_path,
        )
    if pipeline_mode == "staged" and buffer_num != 2:
        return fail("staged pipeline requires buffer_num=2", result_path)

    project_text = os.environ.get("POSE_LETTERBOX720P_PROJECT", "")
    project = Path(project_text).resolve() if project_text else None
    if project is None:
        return fail("POSE_LETTERBOX720P_PROJECT is missing or invalid", result_path)

    # The repository stores the CANN project in ascendc/, while the board's
    # msopgen copy is flattened. Accept both without copying configuration
    # through /tmp or relying on a Python-only switch.
    build_root = project / "ascendc" if (project / "ascendc" / "build.sh").is_file() else project
    if not (build_root / "build.sh").is_file():
        return fail("POSE_LETTERBOX720P_PROJECT is missing or invalid", result_path)

    header = build_root / "op_host" / "pose_letterbox720p_tuning_config.h"
    header.write_text(
        "#pragma once\n\n"
        "// Generated by HGBO for a real package build.\n"
        f"#define POSE_LETTERBOX720P_BUFFER_NUM {buffer_num}U\n"
        f"#define POSE_LETTERBOX720P_OUTPUT_ROWS_PER_TILE {output_rows_per_tile}U\n"
        f"#define POSE_LETTERBOX720P_PIPELINE_MODE {1 if pipeline_mode == 'staged' else 0}U\n",
        encoding="utf-8",
    )

    env = os.environ.copy()
    env["ASCEND_COMPUTE_UNIT"] = "ascend310b"
    build = run(["bash", "build.sh"], build_root, env)
    if build.returncode != 0:
        return fail("build_failed: " + (build.stderr + build.stdout)[-500:], result_path)

    runner = project / "test_pose_letterbox720p_aclnn"
    compile_runner = run([
        "g++", "-std=c++17", "-O2", "test_pose_letterbox720p_aclnn.cpp",
        "-I/usr/local/Ascend/ascend-toolkit/latest/aarch64-linux/include",
        "-Ibuild_out/op_api/include",
        "-L/usr/local/Ascend/ascend-toolkit/latest/aarch64-linux/lib64",
        "-Lbuild_out/op_api/lib", "-lcust_opapi", "-lnnopbase", "-lascendcl",
        "-o", str(runner),
    ], project, env)
    if compile_runner.returncode != 0:
        return fail("runner_build_failed: " + (compile_runner.stderr + compile_runner.stdout)[-500:], result_path)

    env["LD_LIBRARY_PATH"] = f"{project / 'build_out' / 'op_api' / 'lib'}:{env.get('LD_LIBRARY_PATH', '')}"
    execute = run([str(runner)], project, env)
    output = execute.stdout + execute.stderr
    max_abs = parse_metric("max_abs", output)
    mismatched = parse_metric("mismatched_bytes", output)
    p50 = parse_metric("p50_ms", output)
    p95 = parse_metric("p95_ms", output)
    mean = parse_metric("mean_ms", output)
    if execute.returncode != 0 or max_abs != 0 or mismatched != 0 or p50 is None or p95 is None or mean is None:
        return fail("verification_or_benchmark_failed: " + output[-500:], result_path)

    payload = {
        "latency_ms": p50,
        "throughput_fps": 1000.0 / p50,
        "jitter_ms": p95 - p50,
        "cpu_usage": 0.0,
        "memory_usage": 0,
        "correct": True,
        "compile_status": "success_npu_byte_exact",
        "backend": "ascendc_npu",
        "mean_ms": mean,
        "p50_ms": p50,
        "p95_ms": p95,
        "max_abs": 0,
        "mismatched_bytes": 0,
        "config": config,
    }
    result_path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(json.dumps(payload, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
